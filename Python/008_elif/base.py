value = int(input("Please enter the first number: "))
value1 = (input("Enter any of these characters for the specific operation +, -, *, /: "))
value2 = int(input("Please enter the second number: "))
if (value1 == "+"):
    print(value + value2)
if(value1 == "-"):
    print(value - value2)
if(value1 == "*"):
    print(value * value2)
if(value1 == "/"):
    print(value / value2)

    
